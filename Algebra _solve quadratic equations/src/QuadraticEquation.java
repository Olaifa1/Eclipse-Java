// r1= (-b+((b^b)-4*a*c))/2a and r2=(-b-((b^b)-4*a*c))/2a

public class QuadraticEquation {
		private double a;
		private double b;
		private double c;



		public QuadraticEquation() {
			
		}

		public double getA() {
			return a;
		}


		public void setA(double a) {
			this.a = a;
		}


		public double getB() {
			return b;
		}


		public void setB(double b) {
			this.b = b;
		}


		public double getC() {
			return c;
		}


		public void setC(double c) {
			this.c = c;
		}
		
		
			
		
}